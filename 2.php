            <h2>Risk Factors</h2>
            <h3>High blood pressure has many risk factors, including:</h3>

            <ul>
                <li>
                    <b>Age </b>The risk of high blood pressure increases with age. Until about age 64, high blood pressure is more common in men. Women are more likely to develop high blood pressure after age 65.
                </li>


                <li>
                    <b>Obesity or being overweight </b>Excess weight causes changes in the blood vessels, the kidneys and other parts of the body. These changes often increase blood pressure. Being overweight or having obesity also raises the risk of heart disease and its risk factors, such as high cholesterol.

                </li>
                <li>
                    <b>Lack of exercise </b> Not exercising can cause weight gain. Increased weight raises the risk of high blood pressure. People who are inactive also tend to have higher heart rates.
                </li>
                <li>
                    <b>Tobacco use or vaping </b>Smoking, chewing tobacco or vaping immediately raises blood pressure for a short while. Tobacco smoking injures blood vessel walls and speeds up the process of hardening of the arteries
                </li>

                <li>
                    <b>Low potassium levels </b>Potassium helps balance the amount of salt in the body's cells. A proper balance of potassium is important for good heart health.
                </li>
                <li>
                    <b>Drinking too much alcohol </b>Alcohol use has been linked with increased blood pressure, particularly in men.
                </li>
                <li>
                    <b>Stress </b>High levels of stress can lead to a temporary increase in blood pressure. Stress-related habits such as eating more, using tobacco or drinking alcohol can lead to further increases in blood pressure.
                </li>
                <li>
                    <b>Certain chronic conditions </b>Kidney disease, diabetes and sleep apnea are some of the conditions that can lead to high blood pressure.
                </li>
                <li>
                    <b>
                        Pregnancy
                    </b>Sometimes pregnancy causes high blood pressure.
                </li>
            </ul>

