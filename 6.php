                Asthma is a long-term condition affecting children and adults. The air passages in the lungs become narrow due to inflammation and tightening of the muscles around the small airways. This causes asthma symptoms such as cough, wheeze, shortness of breath and chest tightness. These symptoms are intermittent and are often worse at night or during exercise. Other common triggers can make asthma symptoms worse. 
                Triggers vary from person to person, but can include viral infections (colds), dust, smoke, fumes, changes in the weather, grass and tree pollen, animal fur and feathers, strong soaps and perfume.
                Asthma is a condition in which your airways narrow and swell and may produce extra mucus. This can make breathing difficult and trigger coughing, a whistling sound (wheezing) when you breathe out and shortness of breath.
                Asthma can't be cured, but its symptoms can be controlled. Because asthma often changes over time,For some people, asthma is a minor nuisance. For others, it can be a major problem that interferes with daily activities and may lead to a life-threatening asthma attack.

